    /**
     *
     * @param request The servlet request we are processing
     * @param response The servlet response we are creating
     * @param chain The filter chain we are processing
     *
     * @exception IOException if an input/output error occurs
     * @exception ServletException if a servlet error occurs
     */
    public void doFilter(ServletRequest request, ServletResponse response,
            FilterChain chain)
            throws IOException, ServletException {

        if (debug) {
            log("PostArticleFilter:doFilter()");
        }

        doBeforeProcessing(request, response);

        Throwable problem = null;

        HttpServletRequest req = (HttpServletRequest) request;
        HttpServletResponse res = (HttpServletResponse) response;

        String title = req.getParameter("txtTitle");
        String description = req.getParameter("txtDescription");
        String fileUrl = req.getParameter("txtFile");

        TblArticlePostErrors errors = new TblArticlePostErrors();
        boolean foundErr = false;

        try {

            // 1. Check all post typing errors
            if (title.trim().length() > 150) {
                foundErr = true;
                errors.setTitleLengthErr("Title must be less than 150 chars");
            } // end if title length is error
            if (description.trim().length() > 1000) {
                foundErr = true;
                errors.setDescriptionLengthErr("Description must less than 1000 chars");
            } // end if description length is error
            if (fileUrl.trim().length() > 0) {
                if (!fileUrl.endsWith("png") && !fileUrl.endsWith("jpg") && !fileUrl.endsWith("jpeg")) {
                    foundErr = true;
                    errors.setFileInvalidErr("File must be a image (png, jpg, jpeg)");
                } // end if not valid file image
            }
            if (foundErr) {
                // store errors and transfer to page
                Gson gson = new Gson();
                PrintWriter out = res.getWriter();
                res.setContentType("application/json");
                res.setCharacterEncoding("UTF-8");
                String errorsJsonString = gson.toJson(errors);
                out.print(errorsJsonString);
                out.flush();
            } else {
                chain.doFilter(req, res);
            }
        } catch (Throwable t) {
            // If an exception is thrown somewhere down the filter chain,
            // we still want to execute our after processing, and then
            // rethrow the problem after that.
            problem = t;
            t.printStackTrace();
        }

        doAfterProcessing(request, response);

        // If there was a problem, we want to rethrow it if it is
        // a known type, otherwise log it.
        if (problem != null) {
            if (problem instanceof ServletException) {
                throw (ServletException) problem;
            }
            if (problem instanceof IOException) {
                throw (IOException) problem;
            }
            sendProcessingError(problem, response);
        }
    }

